class Barrier(Turtle):
    def __init__(self):
        super().__init__()
        self.hideturtle()
        self.penup()
        self.all_barriers = []
        for barriers in ROAD1_BARRIER_POINTS:
            self.create_barrier(barriers)
        for barriers in ROAD2_BARRIER1_POINTS:
            self.create_barrier(barriers)
        for barriers in ROAD2_BARRIER2_POINTS:
            self.create_barrier(barriers)


    def create_barrier(self, barriers):
        barrier = Turtle()
        barrier.penup()
        barrier.shape("square")
        barrier.goto(barriers)
        barrier.color("saddle brown")
        self.all_barriers.append(barrier)
